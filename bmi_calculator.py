        """‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Å‡∏£‡∏≤‡∏ü‡πÅ‡∏™‡∏î‡∏á‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á BMI"""
        fig, ax = plt.subplots(figsize=(12, 6))
        
        # ‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÅ‡∏ñ‡∏ö‡∏™‡∏µ BMI
        categories = list(self.bmi_categories.keys())
        ranges = [self.bmi_categories[cat]["max"] for cat in categories[:-1]] + [45]
        colors = [self.bmi_categories[cat]["color"] for cat in categories]
        
        # ‡∏ß‡∏≤‡∏î‡πÅ‡∏ñ‡∏ö‡∏™‡∏µ
        prev_val = 0
        for i, (cat, next_val) in enumerate(zip(categories, ranges)):
            ax.barh(0, next_val - prev_val, left=prev_val, height=0.5, 
                   color=colors[i], alpha=0.7, label=cat)
            prev_val = next_val
        
        # ‡∏ó‡∏≥‡πÄ‡∏Ñ‡∏£‡∏∑‡πà‡∏≠‡∏á‡∏´‡∏°‡∏≤‡∏¢‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á BMI ‡∏Ç‡∏≠‡∏á‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ
        ax.plot(bmi, 0, 'ro', markersize=15, label=f'BMI ‡∏Ç‡∏≠‡∏á‡∏Ñ‡∏∏‡∏ì: {bmi}')
        ax.annotate(f'BMI: {bmi}\n{category}', xy=(bmi, 0), xytext=(bmi, 0.3),
                   ha='center', va='bottom', fontsize=12, fontweight='bold',
                   arrowprops=dict(arrowstyle='->', color='red', lw=2))
        
        ax.set_xlim(15, 45)
        ax.set_ylim(-0.5, 0.8)
        ax.set_xlabel('‡∏Ñ‡πà‡∏≤ BMI', fontsize=12)
        ax.set_title('‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡∏Ñ‡πà‡∏≤ BMI ‡∏Ç‡∏≠‡∏á‡∏Ñ‡∏∏‡∏ì', fontsize=14, fontweight='bold')
        ax.legend(bbox_to_anchor=(1.05, 1), loc='upper left')
        ax.grid(True, alpha=0.3)
        
        # ‡∏ã‡πà‡∏≠‡∏ô‡πÅ‡∏Å‡∏ô Y
        ax.set_yticks([])
        
        plt.tight_layout()
        plt.show()

